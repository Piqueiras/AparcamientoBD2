    private void hacerReservaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hacerReservaButtonActionPerformed
        VAviso aviso;
        int horaInicio, horaFin;
        Date Inicio, Fin;
        
        if(jDateChooserFin.getDate()==null){
            aviso=new VAviso(this,true, "Debes seleccionar fecha de Fin");
            aviso.setVisible(true);
            return;
        }  
        int fila=tablaVehiculos.getSelectedRow();
        if(fila==-1){
            aviso=new VAviso(this,true, "No has seleccionado ningun vehiculo");
            aviso.setVisible(true);
            return;
        }

        horaFin=(int) jSpinnerHora.getValue();
        if((horaFin<0)||(horaFin>23)){
            aviso=new VAviso(this,true, "Hora no valida");
            aviso.setVisible(true);
               return;
        }
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        String strDate=formatter.format(jDateChooserFin.getDate());
        String fFin=strDate+" "+String.valueOf(horaFin)+":00:00";
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        try {
            Inicio = new Date();
            Fin=dateFormat.parse(fFin);
        } catch (ParseException e) {
            aviso=new VAviso(this,true, "Hora no valida");
            aviso.setVisible(true);
               return;
        }
        
        
        if(Inicio.compareTo(Fin)>0){
            aviso=new VAviso(this,true, "La fecha de inicio es posterior a la de fin");
            aviso.setVisible(true);
            return;
        }    

        String matricula=tablaVehiculos.getValueAt(fila, 0).toString();
        boolean exito=fa.hacerReserva(matricula, plaza,aparcamiento,Inicio,Fin);
                
        if(exito){
            JOptionPane.showMessageDialog(this,"Reserva realizada con Ã©xito.");
            this.dispose();
            return;
        }    
    }//GEN-LAST:event_hacerReservaButtonActionPerformed

